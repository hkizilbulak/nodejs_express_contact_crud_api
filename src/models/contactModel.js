const mongoose = require("mongoose");

var contactSchema = new mongoose.Schema(
  {
    firstName: {
      type: String,
      required: true,
      trim: true,
    },
    lastName: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    phoneNumber: {
      type: String,
      required: true,
    },
  },
  { collection: "Contact", timestamps: true }
);

module.exports = mongoose.model("Contact", contactSchema);
